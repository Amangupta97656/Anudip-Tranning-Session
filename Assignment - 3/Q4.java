// given number is &gt;, &lt; or = 7.
public class Q4 {
  public static void main(String[] args) {
    int num = 17;
    if (num > 7) {
      System.out.println("The number is greater than 7");
    } else if (num < 7) {
      System.out.println("The number is less than 7");
    } else {
      System.out.println("The number is equal to 7");
    }

  }
}
