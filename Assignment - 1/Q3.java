//Write a program to find sum and average of 3 marks

public class Q3 {
  public static void main(String[] args) {
    int mark1 = 87;
    int mark2 = 93;
    int mark3 = 97;

    int sum = mark1 + mark2 + mark3;
    float average = sum / 3;
    System.out.println(average);
  }
}

/*
 * OUTPUT:
 * 92.0
 */