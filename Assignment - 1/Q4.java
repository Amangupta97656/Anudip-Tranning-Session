//Write a program find quotient and remainder
public class Q4 {
  public static void main(String[] args) {
    float quotient1 = 72 / 5;
    int remainder1 = 72 % 5;

    float quotient2 = 345 / 10;
    int remainder2 = 345 % 10;

    float quotient3 = 345 / 100;
    int remainder3 = 345 % 100;

    System.out.println(quotient1);
    System.out.println(remainder1);

    System.out.println(quotient2);
    System.out.println(remainder2);

    System.out.println(quotient3);
    System.out.println(remainder3);
  }
}

